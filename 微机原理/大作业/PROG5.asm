NAME PROG5

STACK SEGMENT STACK
	STA DB 50 DUP(?)
	STALEN EQU LENGTH STA
STACK ENDS

DATA SEGMENT
	MES DB 20 DUP('A','B')
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE,DS:DATA,ES:DATA,SS:STACK

START:
	MOV AX,DATA
	MOV DS,AX
	MOV ES,AX
	MOV AX,STACK
	MOV SS,AX
	LEA SI,MES
	MOV AH,02H
	MOV DL,[SI]
	INT 21H
	MOV DL,[SI+1]
	INT 21H
	MOV AX,4C00H
	INT 21H
CODE ENDS
END START